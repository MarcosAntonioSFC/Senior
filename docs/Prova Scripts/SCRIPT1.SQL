CREATE TABLE EXAME_NF
(
    IDNF NUMBER (6) NOT NULL,
    NUMERO NUMBER (9) NOT NULL,
    DATACADASTRO DATE,
    TOTALGERAL NUMBER (10,2),
    CONSTRAINT EXAME_NF_PK PRIMARY KEY (IDNF)
);

CREATE TABLE EXAME_ITEMNF
(
    IDITEMNF NUMBER (5) NOT NULL,
    IDNF NUMBER (9) NOT NULL,
    IDPRODUTO NUMBER (9),
    QTDE NUMBER (10,4),
    VALOR NUMBER (10,2),
    CONSTRAINT EXAME_ITEMNF_PK PRIMARY KEY (IDITEMNF, IDNF),
    CONSTRAINT EXAME_ITEMNF_FK1 FOREIGN KEY (IDNF) REFERENCES EXAME_NF (IDNF),
    CONSTRAINT EXAME_ITEMNF_FK2 FOREIGN KEY (IDPRODUTO) REFERENCES PRODUTO (IDPRODUTO)
);